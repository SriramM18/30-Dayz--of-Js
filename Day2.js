// Arthematic Operations 

let a = 24;
let b = 18;

console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a/b);
console.log(a%b);

// Assignment operators

let k = 10; 
k += 12;
console.log(k);
k -= 5;
console.log(k);

// Comparision Operators

console.log(a > b);
console.log(b>a);
console.log(a >= b);
console.log(a <= b);

// == and ===

console.log(a == 24); //true
console.log(a == "24"); //true
console.log(a == 21); //false
console.log(a === 24); //true
console.log(a === "24"); //false

// Logical Operators

console.log(a>10 && b>23);
console.log(a>10 || b>23);
console.log(!a>b);

//Terinary Operators

let res= (a>0)? true: false;
